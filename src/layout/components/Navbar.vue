<template>
  <div class="navbar">
    <hamburger :is-active="sidebar.opened" class="hamburger-container" @toggleClick="toggleSideBar" />

    <breadcrumb class="breadcrumb-container" />

    <div class="right-menu">
      <el-dropdown class="avatar-container" trigger="click">
        <div class="avatar-wrapper">
          <img :src="avatarUrl" class="user-avatar">
          <i class="el-icon-caret-bottom" />
        </div>
        <el-dropdown-menu slot="dropdown" class="user-dropdown">
          <router-link to="/">
            <el-dropdown-item>
              Home
            </el-dropdown-item>
          </router-link>
          <a target="_blank" href="https://github.com/Seeeeck/fast-admin-vue">
            <el-dropdown-item>Github</el-dropdown-item>
          </a>
          <el-dropdown-item divided @click.native="updateProfile">
            <span style="display:block;">Update Profile</span>
          </el-dropdown-item>
          <el-dropdown-item divided @click.native="logout">
            <span style="display:block;">Log Out</span>
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
    </div>
    <user-update
      v-if="userUpdateVisible"
      ref="userUpdate"
      @resetAvatar="handleResetAvatar"
    />
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import Hamburger from '@/components/Hamburger'
import UserUpdate from './user-update'

export default {
  components: {
    Breadcrumb,
    Hamburger,
    UserUpdate
  },
  data() {
    return {
      userUpdateVisible: false,
      avatarUrl: ''
    }
  },
  computed: {
    ...mapGetters([
      'sidebar',
      'avatar'
    ])
  },
  mounted() {
    this.handleAvatars()
  },
  methods: {
    handleResetAvatar() {
      this.$store.dispatch('user/getInfo').then(res => {
        this.handleAvatars()
      })
    },
    handleAvatars() {
      const imgs = require.context('@/assets/avatar/', false, /.(png|jpg|gif)$/)
      const key = imgs.keys().find(key => '@/assets/avatar/' + key.substring(2) === this.avatar)
      this.avatarUrl = imgs(key)
    },
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    async logout() {
      await this.$store.dispatch('user/logout')
      this.$router.push(`/login?redirect=${this.$route.fullPath}`)
    },
    updateProfile() {
      this.userUpdateVisible = true
      this.$nextTick(() => {
        this.$refs.userUpdate.init()
      })
    }
  }
}
</script>

